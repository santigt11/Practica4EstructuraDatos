package modelo;

/**
 *
 * @author Santiago
 */
public class Auto {
    private Integer id;
    private String color;
    private String marca;

    public Auto(Integer id, String color, String marca) {
        this.id = id;
        this.color = color;
        this.marca = marca;
    }

    public Auto() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    } 
}
